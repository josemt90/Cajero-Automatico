
public class Gasto extends Dinero {
	
	public Gasto(double gasto,String description) {
		
		this.dinero=gasto;	
		this.description=description;
	}

     @Override
	public String toString() {                         // devuelve el gasto
		return description + ": "+ dinero +" euros";
	}

 
	


	


	
}
	
	
	




		
		
	

	
	

		
	


		
	

	
	


		
		
	

	
	

	
		









		
	



		
	


		
		
		
	
	
	

	



	
